const BottomNav: React.FC = () => {
  const pathname = usePathname()
  const { unreadCount } = useNotifications()

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-40 bg-bg-dark border-t border-primary-gold/30 safe-bottom">
      <div className="flex items-center justify-around h-16 max-w-md mx-auto">
        {navItems.map((item) => {
          const matchesPrefix = item.matchPrefixes?.some((prefix) => pathname?.startsWith(prefix))
          const isActive = pathname === item.path || matchesPrefix

          return (
            <Link
              key={item.path}
              href={item.path}
              className={clsx(
                'flex flex-col items-center justify-center flex-1 h-full transition-all duration-200',
                'active:scale-95'
              )}
            >
              <motion.div
                className="relative"
                animate={{ scale: isActive ? 1.1 : 1 }}
                transition={{ duration: 0.2 }}
              >
                <div
                  className={clsx(
                    'transition-colors duration-200',
                    isActive ? 'text-primary-gold' : 'text-text-secondary'
                  )}
                >
                  {isActive ? item.activeIcon : item.icon}
                  
                  {/* 红点通知 (显示在"钱包"标签) */}
                  {item.path === '/wallet' && unreadCount > 0 && (
                    <span className="absolute -top-1 -right-1 min-w-[10px] h-[10px] rounded-full bg-red-600 border border-bg-darkest" />
                  )}
                </div>

                {isActive && (
                  <motion.div
                    layoutId="activeTab"
                    className="absolute -bottom-2 left-1/2 w-1 h-1 bg-primary-gold rounded-full"
                    style={{ marginLeft: '-2px' }}
                  />
                )}
              </motion.div>

              <span
                className={clsx(
                  'text-xs mt-1 font-medium transition-colors duration-200',
                  isActive ? 'text-primary-gold' : 'text-text-secondary'
                )}
              >
                {item.label}
              </span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}

export default BottomNav
